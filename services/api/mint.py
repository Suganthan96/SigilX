# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------

def _get_contract():
    """Initialize web3 and the contract instance."""
    w3 = Web3(Web3.HTTPProvider(XLAYER_RPC_URL))
    # X Layer is PoA-based — inject PoA middleware
    w3.middleware_onion.inject(ExtraDataToPOAMiddleware, layer=0)

    if not w3.is_connected():
        raise RuntimeError(f"Cannot connect to X Layer RPC: {XLAYER_RPC_URL}")

    abi = _load_abi()
    contract = w3.eth.contract(
        address=w3.to_checksum_address(CONTRACT_ADDRESS),
        abi=abi,
    )
    return w3, contract


def _load_abi() -> list:
    """Load contract ABI from compiled artifact."""
    if not ABI_PATH.exists():
        raise FileNotFoundError(
            f"Contract ABI not found at {ABI_PATH}. "
            "Run `npm run compile` in the contracts/ directory first."
        )
    with open(ABI_PATH) as f:
        artifact = json.load(f)
    return artifact.get("abi", artifact)


def _extract_token_id(contract, receipt) -> int:
    """Extract tokenId from the PortraitMinted event in the receipt."""
    try:
        logs = contract.events.PortraitMinted().process_receipt(receipt)
        if logs:
            return int(logs[0]["args"]["tokenId"])
    except Exception:
        pass
    return 0


# ---------------------------------------------------------------------------
# Mint status check
# ---------------------------------------------------------------------------

async def get_portrait_token_id(portrait_id: str) -> Optional[int]:
    """
    Check if a portrait has been minted and return its tokenId (or None).
    """
    if not WEB3_AVAILABLE or not CONTRACT_ADDRESS:
        return None
    try:
        _, contract = _get_contract()
        token_id = contract.functions.getTokenByPortraitId(portrait_id).call()
        return int(token_id) if token_id != 0 else None
    except Exception:
        return None
